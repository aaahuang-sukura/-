/*****************************************************************************
* 文件名：delay.c
* 说  明：延时函数
*****************************************************************************/
#include "main.h"

/******************************************************
* 函数：延时1ms函数
* 参数：timer要延时的毫秒数值
*******************************************************/
void delay_ms(uint timer)
{
	uchar j = 124;
	while(timer--)
	{
		for(j = 124; j>0; j--)
		{
			;
		}
	}
}

/******************************************************
* 函数：延时约9us*timer + 6um 
* 参数：timer要延时的timer*9+6 微秒数值
*******************************************************/
void delay_us(uchar timer)
{
	for( ; timer>0; timer--)
	{
		_nop_();
	}
}
